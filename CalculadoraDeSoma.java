public class CalculadoraDeSoma {

    public static void main(String[] args) {
      // Aqui nós vamos realizar uma cálculadora simples de soma entre dois números.
     
      double numero1 = 274.2;
      double numero2 = 192.5;
      //double armazena números com casas decimais.
      
      //agora vamos realizar a soma:
      
      double Soma = numero1 + numero2;
      
      //vamos exibir os resultados:
      
      System.out.println("Resultado da Soma");
      System.out.println("Soma: " + Soma);
      //System.out chama os dados estáticos.
    }
}
